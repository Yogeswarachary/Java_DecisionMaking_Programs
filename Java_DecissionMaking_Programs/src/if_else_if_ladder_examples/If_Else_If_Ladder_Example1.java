public class If_Else_If_Ladder_Example1 {
	public static void main(String[] args) {
		String city="Nijamabad";
		if(city=="Hyderabad"){
			System.out.println("Welcome to telangana");
		}
		else if(city=="Khammam") {
			System.out.println("Welcome to telangana");
		}
		else if(city=="Nijamabad") {
			System.out.println("Welcome to Nijamaabad");
		}
		else if(city=="Adhilabad") {
			System.out.println("Welcome to telangana");
		}
		else if(city=="Warangal") {
			System.out.println("Welcome to telangana");
		}
		else if(city=="Mahabubnagar") {
			System.out.println("Welcome to telangana");
		}
		else {
			System.out.println(city);
		}
	}
}
